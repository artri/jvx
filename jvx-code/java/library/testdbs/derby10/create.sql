  DATETIMEVAL TIMESTAMP NOT NULL WITH DEFAULT '2010-01-01-12.00.00.000000' , 
  NUMBERVAL DECIMAL(11,0) NOT NULL WITH DEFAULT 1234 , 
  TEXT VARCHAR(50) NOT NULL WITH DEFAULT 'TEXT Test' , 
  DATEVAL DATE NOT NULL WITH DEFAULT '2001-01-01' 
