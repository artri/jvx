-- Table: "TEST"
-- DROP TABLE "TEST";

CREATE TABLE TEST
(
  ID bigint NOT NULL GENERATED BY DEFAULT  AS IDENTITY,
  NAME varchar(100),
  CONSTRAINT TEST_PK PRIMARY KEY (ID)
);


-- Table: "TEST_SORT"
-- DROP TABLE "TEST_SORT";

CREATE TABLE TEST_SORT
(
  ID bigint NOT NULL GENERATED BY DEFAULT  AS IDENTITY,
  NAME varchar(100),
  SORT bigint,
  CONSTRAINT TEST_SORT_PK PRIMARY KEY (ID)
);


-- Table: DETAIL

-- DROP TABLE DETAIL;

CREATE TABLE DETAIL
(
  ID bigint NOT NULL GENERATED BY DEFAULT  AS IDENTITY,
  NAME varchar(100),
  TEST_ID bigint,
  CONSTRAINT DETAIL_PK PRIMARY KEY (ID),
  CONSTRAINT TEST_FK FOREIGN KEY (TEST_ID)
      REFERENCES TEST (ID) 
      ON UPDATE NO ACTION ON DELETE NO ACTION
);

-- Table: ADRESSEN

-- DROP TABLE ADRESSEN;

CREATE TABLE ADRESSEN
(
  ID bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  POST_ID bigint NOT NULL,
  STRA_ID bigint NOT NULL,
  HAUSNUMMER decimal(18) NOT NULL,
  TUERNUMMER decimal(18),
  STIEGE decimal(18),
  CONSTRAINT ADRESSEN_PK PRIMARY KEY (ID)
);

-- Table: PERSONEN

-- DROP TABLE PERSONEN;

CREATE TABLE PERSONEN 
(
 ID bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
 VORNAME VARCHAR(100) NOT NULL,
 NACHNAME VARCHAR(100) NOT NULL,
 GEBDAT DATE NOT NULL
);

-- Table: TEST_DEFAULTS

