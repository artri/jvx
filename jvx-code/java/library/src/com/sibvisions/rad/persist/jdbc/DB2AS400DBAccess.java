package com.sibvisions.rad.persist.jdbc;

/**
 * The <code>DB2AS400DBAccess</code> class is an extension of the {@link DB2DBAccess} and uses
 * the AS400 JDBC driver instead of the universal DB2 driver. 
 * 
 * @author René Jahn
 */
public class DB2AS400DBAccess extends DB2DBAccess 
{
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	// Initialization
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

	/**
	 * Constructs a new DB2AS400DBAccess Object.
	 */
	public DB2AS400DBAccess()
	{
		setDriver("com.ibm.as400.access.AS400JDBCDriver");
	}
	
}	// DB2AS400DBAccess
