	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

	/**
	 * Constructs a new CallableHandler.
	 *  
	 * @param pParameterTypes the parameter types.
	 */
