package javax.rad.util.event;

/**
 * The <code>ICallable</code> will be used to call reflective any method. 
 * 
 * @author Martin Handsteiner
 */
public interface ICallable
{
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	// Method definitions
	//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	
	/**
	 * Method that is invoked.
	 * 
	 * @param pParameter the parameters.
	 * @return the return value.
	 * @throws Throwable if invocation fails
	 */
	public Object invoke(Object... pParameter) throws Throwable;
	
}	// ICallable
