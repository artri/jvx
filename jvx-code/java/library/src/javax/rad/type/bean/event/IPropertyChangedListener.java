package javax.rad.type.bean.event;

/**
 * The <code>IPropertyChangedListener</code> is used to inform about property changes of the 
 * {@link javax.rad.type.bean.AbstractBean}.
 * 
 * @author Jozef Dorko
 */
public interface IPropertyChangedListener
{
    //~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    // Method definitions
    //~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    
    /**
     * Notifies that property was changed.
     * 
     * @param pEvent the {@link PropertyChangedEvent}
     * @throws Throwable if error occurs
     */
    public void propertyChanged(PropertyChangedEvent pEvent) throws Throwable;
    
}   // IPropertyChangedListener
